-- Paramètres de base
vim.opt.number = true                  -- Numérotation des lignes
vim.opt.termguicolors = true           -- Activer les vraies couleurs
vim.opt.tabstop = 4                    -- Longueur d'une tabulation
vim.opt.shiftwidth = 4                 -- Longueur d'un shift
vim.opt.expandtab = true               -- Utiliser des espaces au lieu de tabulations
vim.opt.wrap = false                   -- Désactiver le wrapping
vim.opt.scrolloff = 8                  -- Lignes visibles autour du curseur
vim.opt.hlsearch = true                -- Surbrillance des recherches
vim.opt.incsearch = true               -- Recherche incrémentale
vim.opt.ignorecase = true              -- Insensible à la casse
vim.opt.smartcase = true               -- Sensible à la casse si un caractère majuscule est utilisé
vim.opt.cursorline = true              -- Surbrillance de la ligne du curseur
vim.opt.splitright = true              -- Ouvre les fenêtres verticales à droite
vim.opt.splitbelow = true              -- Ouvre les fenêtres horizontales en dessous
vim.opt.mouse = 'a'                    -- Activer la souris
vim.opt.clipboard = 'unnamedplus'      -- Partage du presse-papier avec le système
